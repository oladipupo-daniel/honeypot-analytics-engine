# SCHEMA-AWARE FILTER: Ignores High-Cardinality Unique IDs
# =====================================================================
def filter_high_cardinality_ids(df, cardinality_threshold=0.95):
    """
    Schema-aware filter that removes columns with near-unique values (high-cardinality IDs).
    Heuristic: If a column has >95% unique values relative to row count, it's likely an ID
    and should be excluded from analysis to avoid noise and computational burden.
    
    Args:
        df: Input DataFrame
        cardinality_threshold: Uniqueness ratio threshold (default 0.95 = 95% unique)
    
    Returns:
        DataFrame with ID columns removed, list of filtered column names
    """
    if df.empty:
        return df, []
    
    filtered_cols = []
    id_cols_removed = []
    n_rows = len(df)
    
    for col in df.columns:
        # Calculate cardinality ratio
        unique_ratio = df[col].nunique() / n_rows
        
        # Check if column name hints at ID-like content
        col_lower = str(col).lower()
        id_keywords = ['id', 'uid', 'uuid', 'guid', 'pk', 'primary_key', 'hash', 'identifier']
        is_likely_id = any(keyword in col_lower for keyword in id_keywords)
        
        # Filter out high-cardinality or ID-named columns
        if unique_ratio >= cardinality_threshold or is_likely_id:
            id_cols_removed.append(col)
        else:
            filtered_cols.append(col)
    
    df_filtered = df[filtered_cols].copy()
    return df_filtered, id_cols_removed


