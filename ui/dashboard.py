    def execute_deduction_pipeline(self):
        if self.uploaded_df is None:
            return

        # Clear cache: every time a fresh set of charts is about to be
        # generated, drop any matplotlib figures left over from the
        # previous run so memory usage doesn't grow with each click.
        plt.close('all')

        self.show_loading_screen(self.tab_dashboard)
        self.show_loading_screen(self.tab_pivot)
        self.show_loading_screen(self.tab_forecast)
        self.update_idletasks()
        
        # 1. Logic Processing
        self.analysis_context = detect_dataset_context(self.uploaded_df)
        metric_col = self.analysis_context['metric_column']
        current_val = self.uploaded_df[metric_col].iloc[-1]
        avg_val = self.uploaded_df[metric_col].mean()
        
        # Run ARIMA
        status, forecast_val = compute_arima_forecast(self.uploaded_df[metric_col])
        narrative = get_decision_narrative(metric_col, current_val, forecast_val, avg_val)
        
        # 2. UI Cleanup (Destroy widgets AFTER calculation)
        for widget in self.tab_dashboard.winfo_children():
            widget.destroy()
        for widget in self.tab_pivot.winfo_children():
            widget.destroy()
        for widget in self.tab_forecast.winfo_children():
            widget.destroy()
        
        # 3. UI Update (Update label AFTER cleanup, so it doesn't get destroyed)
        self.whisper_lbl.configure(text=narrative)
        
        # 4. Rendering
        self.export_btn.configure(state="normal")
        self.render_dashboard_view()
        self.render_systemic_pivot_view()
        self.render_forecasting_matrix()
        
        messagebox.showinfo("Deduction Finished", "Analytics generated.")


