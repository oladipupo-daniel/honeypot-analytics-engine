    def compute_forecast_results(self):
        """Runs all real diagnostics once and caches them for the forecast tab."""
        metric = self.analysis_context.get("metric_column")
        entity = self.analysis_context.get("entity_column")

        # Single-record datasets (one row, many columns) have no sequence to
        # forecast along - route them to the variance-based summary instead
        # of letting Kalman/entropy/Lyapunov math run on a single point.
        if is_single_record(self.uploaded_df):
            variance_result = run_variance_summary(self.uploaded_df, metric)
            if variance_result is not None:
                variance_result["metric"] = metric
                variance_result["entity"] = entity
                self.forecast_results = variance_result
                return variance_result

        series = self.uploaded_df[metric] if metric in self.uploaded_df.columns else self.uploaded_df.select_dtypes(include="number").iloc[:, 0]

        filtered, forecast = kalman_filter_smooth(series)
        lyap, lyap_verdict = estimate_lyapunov_exponent(series)
        flavor = detect_data_flavor(self.uploaded_df, metric)

        results = {
            "metric": metric,
            "entity": entity,
            "flavor": flavor,
            "series": series,
            "kalman_filtered": filtered,
            "kalman_forecast": forecast,
            "entropy_rate": compute_entropy_rate(series),
            "lyapunov": (lyap, lyap_verdict),
            "phase_space": phase_space_embedding(series),
            "ergodicity": ergodicity_check(series),
            "martingale": martingale_fairness_test(series),
            "shrinkage": bayesian_hierarchical_shrinkage(self.uploaded_df, entity, metric) if entity in self.uploaded_df.columns else None,
        }
        self.forecast_results = results
        return results

    def _auto_map_columns_fallback(self, df):
        """Guess the target, category, and engagement columns when explicit mapping is unavailable."""
        target = detect_target_metric_column(df)
        category = detect_entity_column(df, exclude={target} if target else None)

        engagement_tokens = [
            'engagement', 'click', 'view', 'impression', 'ctr',
            'conversion', 'activity', 'interaction', 'session'
        ]
        engagement = [c for c in df.columns if any(token in str(c).lower() for token in engagement_tokens) and c != target]

        return {'target': target, 'category': category, 'engagement': engagement}

    def compute_honeypot_analysis(self):
        if self.uploaded_df is None:
            return {}

        df = self.uploaded_df.copy()
        mapping = self._auto_map_columns_fallback(df)

        if not mapping.get('target'):
            return {"error": "Could not identify a performance metric in this file."}

        target_col = mapping['target']
        df[target_col] = pd.to_numeric(df[target_col], errors='coerce').fillna(0)

        score = df[target_col] * 0.1
        for eng_col in mapping.get('engagement', []) or []:
            if eng_col in df.columns:
                df[eng_col] = pd.to_numeric(df[eng_col], errors='coerce').fillna(0)
                score += df[eng_col] * 2.0

        arima_status, arima_forecast = compute_arima_forecast(df[target_col])
        mean_reversion_value = (
            float(df[target_col].iloc[-1] + (df[target_col].iloc[-1] - df[target_col].iloc[-2]))
            if len(df[target_col]) > 1 else float(df[target_col].iloc[-1])
        )

        top_cat = "N/A"
        if mapping.get('category') in df.columns:
            try:
                top_cat = str(df.groupby(mapping['category'])[target_col].mean().idxmax())
            except Exception:
                top_cat = "N/A"

        return {
            "target_col": target_col,
            "category": mapping.get('category'),
            "engagement": mapping.get('engagement'),
            "c_r": score.mean() / (df[target_col].std() + 1),
            "stability": score.mean(),
            "regime_shift": "YES" if df[target_col].max() > (df[target_col].mean() * 3) else "NO",
            "top_cat": top_cat,
            "arima_forecast": arima_forecast,
            "arima_status": arima_status,
            "mean_reversion_forecast": [mean_reversion_value]
        }

