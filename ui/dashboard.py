    def render_classroom_diagnostics_panel(self, pivot_container, cd):
        """
        "Classroom Diagnostic" section of the Systemic Audit Pivotal page:
        Top/Bottom performers, Performance Gap, Z-Score Benchmarking,
        Pass/Fail Distribution, Class Variance, and Difficulty Analysis -
        all measured against the group's own baseline (context-aware),
        never a fitted trend. Conditional formatting (green/amber/red)
        highlights who needs attention vs who is excelling at a glance.
        """
        section = ctk.CTkFrame(pivot_container, fg_color=self.bg_panel, corner_radius=16)
        section.pack(fill="both", expand=True, padx=10, pady=(0, 15))

        ctk.CTkLabel(
            section, text="📊 Classroom / Performance Diagnostic",
            font=ctk.CTkFont(family="Segoe UI", size=15, weight="bold"), text_color=self.accent_color
        ).pack(anchor="w", padx=16, pady=(14, 2))

        ctk.CTkLabel(
            section,
            text=("Each row is compared to the group's own mean and standard deviation - not a fitted "
                  "trend - so nothing here mistakes ordinary spread for a false pattern."),
            font=ctk.CTkFont(family="Segoe UI", size=10, slant="italic"), text_color="#94A3B8",
            wraplength=880, justify="left"
        ).pack(anchor="w", padx=16, pady=(0, 10))

        # --- Stat cards row: Performance Gap / Pass-Fail / Class Variance ---
        stats_row = ctk.CTkFrame(section, fg_color="transparent")
        stats_row.pack(fill="x", padx=12, pady=(0, 10))

        cards = [
            ("Performance Gap", f"{cd['performance_gap']:.2f}",
             f"Highest {cd['highest_value']:.2f} vs Lowest {cd['lowest_value']:.2f}"),
            ("Pass/Fail Distribution", f"{cd['pass_rate']:.1f}% pass",
             f"{cd['pass_count']} pass / {cd['fail_count']} fail (threshold {cd['pass_threshold']:.2f})"),
            ("Class Variance", f"{cd['class_variance']:.2f}",
             f"Std Dev {cd['class_std_dev']:.2f} · Mean {cd['class_mean']:.2f}"),
        ]
        for title, big, sub in cards:
            card = ctk.CTkFrame(stats_row, fg_color="#111524", corner_radius=12)
            card.pack(side="left", expand=True, fill="both", padx=6, pady=4)
            ctk.CTkLabel(card, text=big, font=ctk.CTkFont(family="Segoe UI", size=18, weight="bold"),
                         text_color="#FFFFFF").pack(pady=(12, 0))
            ctk.CTkLabel(card, text=title, font=ctk.CTkFont(family="Segoe UI", size=11, weight="bold"),
                         text_color=self.accent_color).pack()
            ctk.CTkLabel(card, text=sub, font=ctk.CTkFont(family="Segoe UI", size=9),
                         text_color="#94A3B8", wraplength=220, justify="center").pack(pady=(0, 12))

        # --- Difficulty analysis line ---
        if cd.get("hardest_column") and cd.get("easiest_column"):
            diff_text = (
                f"Difficulty Analysis: '{cd['hardest_column']['column'].title()}' has the lowest average "
                f"({cd['hardest_column']['mean']:.2f}) — the toughest area. "
                f"'{cd['easiest_column']['column'].title()}' has the highest average "
                f"({cd['easiest_column']['mean']:.2f}) — the strongest area."
            )
            ctk.CTkLabel(section, text=diff_text, font=ctk.CTkFont(family="Segoe UI", size=10),
                         text_color="#CBD5E1", wraplength=880, justify="left").pack(anchor="w", padx=16, pady=(0, 10))

        # --- Top 5 / Bottom 5 chart with conditional-formatting colors ---
        fig = plt.Figure(figsize=(8.5, 4), dpi=100)
        fig.patch.set_facecolor(self.bg_panel)
        ax = fig.add_subplot(111)
        ax.set_facecolor("#111524")

        top = cd["top_performers"]
        bottom = cd["bottom_performers"]
        combined = top + list(reversed(bottom))
        entities = [str(r["entity"]) for r in combined]
        vals = [r["value"] for r in combined]
        # Conditional formatting: green for top performers, red for bottom performers
        bar_colors = ["#22C55E"] * len(top) + ["#EF4444"] * len(bottom)

        bars = ax.bar(entities, vals, color=bar_colors, edgecolor="#0F172A")
        ax.axhline(cd["class_mean"], color="#F59E0B", linestyle="--", linewidth=1.5,
                   label=f"Class Mean = {cd['class_mean']:.2f}")
        ax.set_title(f"Top {len(top)} vs Bottom {len(bottom)} Performers (conditional formatting: green = top, red = bottom)",
                     color="#FFFFFF", fontsize=10, weight="bold")
        ax.tick_params(axis='x', labelcolor="#94A3B8", labelsize=8, rotation=25)
        ax.tick_params(axis='y', labelcolor="#94A3B8", labelsize=8)
        ax.legend(loc="upper right", fontsize=8, framealpha=0.2)
        ax.grid(True, axis='y', color='#2D3748', linestyle=':', alpha=0.4)
        fig.tight_layout()

        chart_holder = ctk.CTkFrame(section, fg_color="transparent")
        chart_holder.pack(fill="both", expand=True, padx=10, pady=(0, 16))
        canvas = FigureCanvasTkAgg(fig, master=chart_holder)
        self.attach_hover_tooltip(fig, ax, list(bars), [f"{e}: {v:.2f}" for e, v in zip(entities, vals)])
        canvas.draw()
        canvas.get_tk_widget().pack(fill="both", expand=True)

    def render_single_record_pivot_chart(self, pivot_container, reliability_data):
        """
        Systemic Audit Pivotal view for a single-record dataset: since there
        is only one row, this draws the variance-based breakdown (per-field
        values against the record's own mean) instead of a cross-row
        correlation/density chart.
        """
        fig = plt.Figure(figsize=(8, 5.2), dpi=100)
        fig.patch.set_facecolor(self.bg_panel)
        ax1 = fig.add_subplot(111)
        ax1.set_facecolor('#111524')
        ax1.chart_id = "SINGLE_RECORD_VARIANCE_MATRIX"

        per_column = reliability_data["per_column_values"]
        labels = [k.title() for k in per_column.keys()]
        values = list(per_column.values())
        mean_val = reliability_data["mean"]

        bars = ax1.bar(labels, values, color="#38BDF8", alpha=0.8, edgecolor="#0284C7")
        ax1.axhline(mean_val, color="#F59E0B", linestyle="--", linewidth=1.5,
                    label=f"Record Mean = {mean_val:.2f}")
        bar_labels = [f"{lbl}: {val:.2f}" for lbl, val in zip(labels, values)]

        ax1.set_title(
            f"Single-Record Variance Matrix (Variance = {reliability_data['variance']:.4f}, "
            f"Std Dev = {reliability_data['std_dev']:.4f})\n"
            f"1 row detected - reported by variance across {len(labels)} fields",
            color="#FFFFFF", fontsize=11, weight="bold", loc="left", pad=12
        )
        ax1.set_ylabel("Field Value", color="#94A3B8", fontsize=9, weight="bold")
        ax1.tick_params(axis='x', labelcolor="#94A3B8", labelsize=8, rotation=20)
        ax1.tick_params(axis='y', labelcolor="#94A3B8", labelsize=8)
        ax1.legend(loc="upper right", fontsize=8, framealpha=0.2)
        ax1.grid(True, axis='y', color='#2D3748', linestyle=':', alpha=0.4)

        self.add_tap_watermark(fig)
        fig.tight_layout()
        canvas = FigureCanvasTkAgg(fig, master=pivot_container)
        self.attach_hover_tooltip(fig, ax1, list(bars), bar_labels)
        canvas.draw()
        canvas.get_tk_widget().pack(fill="both", expand=True, padx=15, pady=15)
        fig.canvas.mpl_connect("button_press_event", self.on_canvas_click)

