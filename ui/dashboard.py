    def render_systemic_pivot_view(self):
        """Generates a factual 5-chart matrix for football or a universal Reliability Matrix for generic data."""
        pivot_container = ctk.CTkScrollableFrame(self.tab_pivot, fg_color="transparent")
        pivot_container.pack(fill="both", expand=True, padx=5, pady=5)
        
        if self.uploaded_df is None or self.uploaded_df.empty:
            return
            
        df = self.uploaded_df.copy()
        df.columns = [str(c).lower().strip() for c in df.columns]
        
        plt.style.use('dark_background' if self.night_vision_mode else 'default')
        
        if self.analysis_context["type"] == "SPORTS STATISTICS & PERFORMANCE LEAGUE":
            df['fthg'] = pd.to_numeric(df['fthg'], errors='coerce').fillna(0).astype(int)
            df['ftag'] = pd.to_numeric(df['ftag'], errors='coerce').fillna(0).astype(int)
            df['total_goals'] = df['fthg'] + df['ftag']
            
            fig = plt.Figure(figsize=(12, 7.8), dpi=95)
            fig.patch.set_facecolor(self.bg_panel)
            gs = gridspec.GridSpec(3, 2, figure=fig, height_ratios=[1, 1, 1.1])
            
            team_home_gf = df.groupby('hometeam')['fthg'].sum()
            team_away_gf = df.groupby('awayteam')['ftag'].sum()
            top_teams = (team_home_gf + team_away_gf).sort_values(ascending=False).head(6).index.tolist()
            
            # CHART 1: Real Cross-Tabulation Grid
            ax1 = fig.add_subplot(gs[0, 0])
            ax1.chart_id = 1
            pivot_df = df[df['hometeam'].isin(top_teams) & df['awayteam'].isin(top_teams)].pivot_table(
                values='total_goals', index='awayteam', columns='hometeam', aggfunc='mean'
            ).fillna(0)
            cax = ax1.imshow(pivot_df.values, cmap="Reds", aspect="auto", origin="lower")
            ax1.set_title("1. Home vs Away Matrix Grid (Mean Goals Matrix)", fontsize=9, color=self.accent_color, weight="bold")
            ax1.set_xticks(range(len(pivot_df.columns)))
            ax1.set_xticklabels(pivot_df.columns, rotation=15, ha='right', fontsize=7)
            ax1.set_yticks(range(len(pivot_df.index)))
            ax1.set_yticklabels(pivot_df.index, fontsize=7)
            fig.colorbar(cax, ax=ax1, orientation='vertical', shrink=0.75).ax.tick_params(labelsize=6)

            # CHART 2: Match Divergence Scatter Grid
            ax2 = fig.add_subplot(gs[0, 1])
            ax2.chart_id = 2
            ax2.scatter(df['fthg'], df['ftag'], alpha=0.3, color="#38BDF8", edgecolors='none', s=45)
            max_limit = max(df['fthg'].max(), df['ftag'].max()) + 1
            ax2.plot([0, max_limit], [0, max_limit], color="#EF4444", linestyle="--", alpha=0.6, label="Equilibrium Line (Draw)")
            ax2.set_title("2. Score Divergence Coordinate Grid (FTHG vs FTAG)", fontsize=9, color=self.accent_color, weight="bold")
            ax2.set_xlim(-0.5, max_limit)
            ax2.set_ylim(-0.5, max_limit)
            ax2.tick_params(labelsize=7, colors="#94A3B8")
            ax2.legend(loc="upper right", fontsize=6, framealpha=0.2)
            ax2.grid(True, alpha=0.05)

            # CHART 3: Chronological Cumulative Volume Production Line
            ax3 = fig.add_subplot(gs[1, 0])
            ax3.chart_id = 3
            df['cum_home_goals'] = df['fthg'].cumsum()
            df['cum_away_goals'] = df['ftag'].cumsum()
            ax3.plot(df.index, df['cum_home_goals'], color="#F59E0B", linewidth=1.8, label="Cumulative Home Production")
            ax3.plot(df.index, df['cum_away_goals'], color="#10B981", linewidth=1.8, label="Cumulative Away Production")
            ax3.set_title("3. Chronological Cumulative Production (Temporal Mass)", fontsize=9, color=self.accent_color, weight="bold")
            ax3.tick_params(labelsize=7, colors="#94A3B8")
            ax3.legend(loc="upper left", fontsize=7, framealpha=0.2)
            ax3.grid(True, alpha=0.05)
            

            # CHART 4: Goal Differential Distribution Spectrum
            ax4 = fig.add_subplot(gs[1, 1])
            ax4.chart_id = 4
            goal_diffs = df['fthg'] - df['ftag']
            unique_diffs, counts = np.unique(goal_diffs, return_counts=True)
            ax4.bar(unique_diffs, counts, color="#EC4899", alpha=0.8, width=0.6, edgecolor="#312E81")
            ax4.set_title("4. Empirical Differential Probability Vector Distribution", fontsize=9, color=self.accent_color, weight="bold")
            ax4.tick_params(labelsize=7, colors="#94A3B8")
            ax4.grid(True, alpha=0.05)

            # CHART 5: Clustered Comparative Team Production Vector Layout
            ax5 = fig.add_subplot(gs[2, :])
            ax5.chart_id = 5
            home_yields = [team_home_gf[t] for t in top_teams]
            away_yields = [team_away_gf[t] for t in top_teams]
            x_indices = np.arange(len(top_teams))
            w_bar = 0.35
            home_bars = ax5.bar(x_indices - w_bar/2, home_yields, w_bar, label='Gross Home Production Volume (FTHG)', color="#38BDF8")
            away_bars = ax5.bar(x_indices + w_bar/2, away_yields, w_bar, label='Gross Away Production Volume (FTAG)', color="#A855F7")
            ax5.set_title("5. Core Node Venue Volumetric Output Performance Breakdown Matrix", fontsize=10, color=self.accent_color, weight="bold")
            ax5.set_xticks(x_indices)
            ax5.set_xticklabels(top_teams, fontsize=8, rotation=10)
            ax5.tick_params(labelsize=7, colors="#94A3B8")
            ax5.legend(loc="upper right", fontsize=7, ncol=2, framealpha=0.2)
            ax5.grid(True, alpha=0.05)

            cluster_artists = list(home_bars) + list(away_bars)
            cluster_labels = (
                [f"{team} (Home): {val:.0f} goals" for team, val in zip(top_teams, home_yields)] +
                [f"{team} (Away): {val:.0f} goals" for team, val in zip(top_teams, away_yields)]
            )
            self.add_tap_watermark(fig)
            fig.tight_layout()
            canvas = FigureCanvasTkAgg(fig, master=pivot_container)
            self.attach_hover_tooltip(fig, ax5, cluster_artists, cluster_labels)
            canvas.draw()
            canvas.get_tk_widget().pack(fill="both", expand=True, padx=5, pady=5)
            fig.canvas.mpl_connect("button_press_event", self.on_canvas_click)
            
