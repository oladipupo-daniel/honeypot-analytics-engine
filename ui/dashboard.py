    def render_momentum_chart(self, parent_frame, df, metric_col, window=5):
        """
        Momentum tracker: shows whether the metric is currently trending above or
        below its own rolling average — a mirrored area chart, shaded by direction.
        Works for any numeric metric, not just sports scorelines.
        """
        fig, ax = plt.subplots(figsize=(6, 2.2), dpi=100)
        fig.patch.set_facecolor("#0F172A")
        ax.set_facecolor("#0F172A")

        rolling_avg = df[metric_col].rolling(window, min_periods=1).mean()
        momentum = df[metric_col] - rolling_avg

        ax.fill_between(df.index, momentum, 0, where=(momentum >= 0),
                         color="#22C55E", alpha=0.65, label="Above rolling average")
        ax.fill_between(df.index, momentum, 0, where=(momentum < 0),
                         color="#EF4444", alpha=0.65, label="Below rolling average")
        ax.axhline(0, color="#475569", linewidth=1, linestyle="--")

        ax.set_title(f"Momentum Tracker: {metric_col} vs its {window}-period average",
                      color="white", fontsize=9)
        ax.spines['top'].set_visible(False)
        ax.spines['right'].set_visible(False)
        ax.tick_params(colors="#94A3B8", labelsize=7)
        ax.legend(loc="upper right", fontsize=7, framealpha=0.2, labelcolor="white")

        canvas = FigureCanvasTkAgg(fig, master=parent_frame)
        self.attach_line_hover_tooltip(
            fig, ax, None, df.index, momentum.values,
            value_fmt="momentum: {:+.2f}"
        )
        plt.close(fig)
        return canvas, momentum

