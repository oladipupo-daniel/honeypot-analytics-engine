    def attach_hover_tooltip(self, fig, ax, artists, labels):
        """
        Generic hover tooltip for any matplotlib chart embedded in a Tk canvas.
        - artists: list of matplotlib artists to track (bars, or a Line2D)
        - labels: list of strings, same length/order as artists, shown on hover
        Works for bar charts (each bar is one artist) and line charts (pass the
        Line2D itself plus per-point labels and let mplhover do nearest-point lookup).
        """
        annot = ax.annotate(
            "", xy=(0, 0), xytext=(12, 12), textcoords="offset points",
            bbox=dict(boxstyle="round,pad=0.4", fc="#0F172A", ec=self.accent_color, lw=1),
            color="#F1F5F9", fontsize=8, family="Segoe UI"
        )
        annot.set_visible(False)

        def on_move(event):
            if event.inaxes != ax:
                if annot.get_visible():
                    annot.set_visible(False)
                    fig.canvas.draw_idle()
                return

            for artist, label in zip(artists, labels):
                contains, _ = artist.contains(event)
                if contains:
                    if hasattr(artist, "get_xy"):  # Rectangle (bar)
                        x0, y0 = artist.get_xy()
                        w, h = artist.get_width(), artist.get_height()
                        annot.xy = (x0 + w, y0 + h / 2)
                    else:
                        annot.xy = (event.xdata, event.ydata)
                    annot.set_text(label)
                    annot.set_visible(True)
                    fig.canvas.draw_idle()
                    return

            if annot.get_visible():
                annot.set_visible(False)
                fig.canvas.draw_idle()

        fig.canvas.mpl_connect("motion_notify_event", on_move)

    def attach_line_hover_tooltip(self, fig, ax, line, x_vals, y_vals, value_fmt="{:.2f}"):
        """Hover tooltip for a single line plot: snaps to the nearest x point."""
        annot = ax.annotate(
            "", xy=(0, 0), xytext=(12, 12), textcoords="offset points",
            bbox=dict(boxstyle="round,pad=0.4", fc="#0F172A", ec=self.accent_color, lw=1),
            color="#F1F5F9", fontsize=8, family="Segoe UI"
        )
        annot.set_visible(False)
        x_arr = np.asarray(x_vals)

        def on_move(event):
            if event.inaxes != ax or event.xdata is None:
                if annot.get_visible():
                    annot.set_visible(False)
                    fig.canvas.draw_idle()
                return

            idx = int(np.argmin(np.abs(x_arr - event.xdata)))
            if idx >= len(y_vals):
                return

            annot.xy = (x_arr[idx], y_vals[idx])
            annot.set_text(f"x={x_arr[idx]}\ny={value_fmt.format(y_vals[idx])}")
            annot.set_visible(True)
            fig.canvas.draw_idle()

        fig.canvas.mpl_connect("motion_notify_event", on_move)

